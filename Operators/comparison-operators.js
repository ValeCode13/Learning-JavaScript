// Comparison Operator - we use this to compare value of varible with something else.


// The result of expression with a 'comparison operator' is 'boolean' - True or False.
let x = 1;

// Relational Operators:
console.log(x > 0);
console.log(x >= 1); // True
console.log(x < 1); // False
console.log(x <= 1); // False

// Equality Operators:
console.log (x === 1); // to checked if x is equal to one.
console.log (x !== 1); // to checked if x is 'not' equal to one.
